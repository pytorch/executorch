TEST_F(AOTITorchInt4PlainMMTest, NullInputHandling) {
  int64_t M = 2, K = 256, N = 64, gs = 32;
  int64_t ng = K / gs;
  int64_t n_super = K / 256;

  Tensor* A = create_bf16({M, K});
  Tensor* qdata = create_uint8({N, K / 2});
  Tensor* scale = create_uint8({N, ng});
  Tensor* scale_step = create_fp16({N, n_super});
  Tensor* zero = create_uint8({N, ng});
  Tensor* zero_point_step = create_fp16({N, n_super});
  Tensor* output = nullptr;

  EXPECT_EQ(
      aoti_torch_cuda_int4_plain_mm(
          nullptr,
          qdata,
          scale,
          scale_step,
          zero,
          zero_point_step,
          gs,
          &output),
      Error::InvalidArgument);
  EXPECT_EQ(
      aoti_torch_cuda_int4_plain_mm(
          A, nullptr, scale, scale_step, zero, zero_point_step, gs, &output),
      Error::InvalidArgument);
  EXPECT_EQ(
      aoti_torch_cuda_int4_plain_mm(
          A, qdata, nullptr, scale_step, zero, zero_point_step, gs, &output),
      Error::InvalidArgument);
  EXPECT_EQ(
      aoti_torch_cuda_int4_plain_mm(
          A, qdata, scale, nullptr, zero, zero_point_step, gs, &output),
      Error::InvalidArgument);
  EXPECT_EQ(
      aoti_torch_cuda_int4_plain_mm(
          A, qdata, scale, scale_step, nullptr, zero_point_step, gs, &output),
      Error::InvalidArgument);
  EXPECT_EQ(
      aoti_torch_cuda_int4_plain_mm(
          A, qdata, scale, scale_step, zero, nullptr, gs, &output),
      Error::InvalidArgument);
  EXPECT_EQ(
      aoti_torch_cuda_int4_plain_mm(
          A, qdata, scale, scale_step, zero, zero_point_step, gs, nullptr),
      Error::InvalidArgument);
}

// RejectsNativeLayout: scale/zero in the un-transposed native [n_groups, N]
// layout (instead of coalesced [N, n_groups]) must be rejected gracefully.
// K=256, gs=32 -> n_groups=8, N=16; native scale [8,16] vs expected [16,8].
TEST_F(AOTITorchInt4PlainMMTest, RejectsNativeLayout) {
  int64_t M = 1, K = 256, N = 16, gs = 32;
  int64_t ng = K / gs; // 8
  int64_t n_super = K / 256; // 1

  Tensor* A = create_bf16({M, K});
  Tensor* qdata = create_uint8({N, K / 2});
  // Native torchao layout [n_groups, N] = [8, 16], NOT coalesced [16, 8].
  Tensor* scale = create_uint8({ng, N});
  Tensor* scale_step = create_fp16({N, n_super});
  Tensor* zero = create_uint8({ng, N});
  Tensor* zero_point_step = create_fp16({N, n_super});
  Tensor* output = nullptr;

  EXPECT_EQ(
      aoti_torch_cuda_int4_plain_mm(
          A, qdata, scale, scale_step, zero, zero_point_step, gs, &output),
      Error::InvalidArgument);
}
