TEST_F(AOTITorchInt5PlainMMTest, NullInputHandling) {
  int64_t M = 2, K = 256, N = 64, gs = 32;
  int64_t ng = K / gs;
  int64_t n_super = K / 256;

  Tensor* A = create_bf16({M, K});
  Tensor* ql = create_uint8({N, K / 2});
  Tensor* qh = create_uint8({N, K / 8});
  Tensor* scale = create_uint8({N, ng});
  Tensor* scale_step = create_fp16({N, n_super});
  Tensor* zero = create_uint8({N, ng});
  Tensor* zero_point_step = create_fp16({N, n_super});
  Tensor* output = nullptr;

  EXPECT_EQ(
      aoti_torch_cuda_int5_plain_mm(
          nullptr,
          ql,
          qh,
          scale,
          scale_step,
          zero,
          zero_point_step,
          gs,
          &output),
      Error::InvalidArgument);
  EXPECT_EQ(
      aoti_torch_cuda_int5_plain_mm(
          A,
          nullptr,
          qh,
          scale,
          scale_step,
          zero,
          zero_point_step,
          gs,
          &output),
      Error::InvalidArgument);
  EXPECT_EQ(
      aoti_torch_cuda_int5_plain_mm(
          A,
          ql,
          nullptr,
          scale,
          scale_step,
          zero,
          zero_point_step,
          gs,
          &output),
      Error::InvalidArgument);
  EXPECT_EQ(
      aoti_torch_cuda_int5_plain_mm(
          A, ql, qh, nullptr, scale_step, zero, zero_point_step, gs, &output),
      Error::InvalidArgument);
  EXPECT_EQ(
      aoti_torch_cuda_int5_plain_mm(
          A, ql, qh, scale, nullptr, zero, zero_point_step, gs, &output),
      Error::InvalidArgument);
  EXPECT_EQ(
      aoti_torch_cuda_int5_plain_mm(
          A, ql, qh, scale, scale_step, nullptr, zero_point_step, gs, &output),
      Error::InvalidArgument);
  EXPECT_EQ(
      aoti_torch_cuda_int5_plain_mm(
          A, ql, qh, scale, scale_step, zero, nullptr, gs, &output),
      Error::InvalidArgument);
  EXPECT_EQ(
      aoti_torch_cuda_int5_plain_mm(
          A, ql, qh, scale, scale_step, zero, zero_point_step, gs, nullptr),
      Error::InvalidArgument);
}
