// W5A8 dp4a matvec shim for packed INT5 decode (CudaDp4aPlanarInt5Tensor
// layout, GGUF Q5_K). The 5-bit weight is split into two planes:
//   ql         : [N, K/2] uint8 low-nibble plane (nibble-packed even/odd)
//   qh         : [N, K/8] uint8 high-1-bit plane (per 32-weight chunk: 4 bytes,
//                one per dp4a word; low nibble = even highs, high nibble = odd)
// Metadata (z_pack, produced by CudaDp4aPlanarInt5Tensor._from_intx_int8):
//   scale      : [N, K/gs] uint8 per-group scale codes
//   scale_step : [N, K/256] fp16 per-256-super-block step; group scale =
//                scale_code * scale_step[:, g/(256/gs)] (step loaded once per
//                super-block, register-cached across its 8 groups).
//   zero       : [N, K/gs] uint8 per-group zero codes
//   zero_point_step  : [N, K/256] fp16 per-256-super-block step; group zero =
//                zero_code * zero_point_step[:, g/(256/gs)]. Both fp16 steps
//                are packed into ONE 32-bit warp-shuffle word by the subgroup
//                leader (z_pack) and broadcast to the 8-lane subgroup — the
//                single most format-critical detail this test covers.
// Q5_K is asymmetric: the stored 5-bit value is the raw unsigned u in [0, 31]
// and a per-group zero point is subtracted in the kernel (like INT4).
// Test vectors are generated from the production pack path
// (IntxUnpackedToInt8Tensor-shaped source -> CudaDp4aPlanarInt5Tensor) and
// expected[] from the tensor's own bf16 dequant @ F.linear (same math the M>4
// dispatch reference computes). The kernel runs W5A8 (it also quantizes
// activations to int8), so a 0.5 atol absorbs the activation-quant noise. The
// cases cover a single super-block, multiple super-blocks (exercising the
// per-256 step advance), wide N, and a 4-super-block case that stresses the
// z_pack packed-shuffle-word broadcast across full warps.
//
// These arrays are machine-generated; do NOT hand-edit them. Regenerate with
//   python backends/cuda/runtime/shims/tests/gen_plain_mm_test_vectors.py \
//         --dtype int5
// and paste the emitted blocks; verify with `--dtype int5 --check` (the
// generator uses a single fixed seed and draw order, so the output is exact).
class AOTITorchInt5PlainMMTest : public ::testing::Test {
 protected:
  void SetUp() override {
    et_pal_init();

    int device_count = 0;
    cudaError_t err = cudaGetDeviceCount(&device_count);
    if (err != cudaSuccess || device_count == 0) {
      GTEST_SKIP() << "CUDA not available";
    }
  }

  Tensor* create_tensor(
      const std::vector<int64_t>& sizes,
      slim_c10::ScalarType dtype) {
    Tensor* tensor;
    AOTITorchError error = aoti_torch_empty_strided(
        sizes.size(),
        sizes.data(),
        nullptr,
        static_cast<int32_t>(dtype),
        static_cast<int32_t>(slim_c10::DeviceType::CUDA),
        0,
        &tensor);
    return (error == Error::Ok) ? tensor : nullptr;
  }

  Tensor* create_bf16(const std::vector<int64_t>& sizes) {
    return create_tensor(sizes, slim_c10::ScalarType::BFloat16);
  }

  Tensor* create_fp16(const std::vector<int64_t>& sizes) {
    return create_tensor(sizes, slim_c10::ScalarType::Half);
  }

  Tensor* create_uint8(const std::vector<int64_t>& sizes) {
    return create_tensor(sizes, slim_c10::ScalarType::Byte);
  }

  void upload(Tensor* t, const void* host_data, size_t bytes) {
    cudaMemcpy(t->data_ptr(), host_data, bytes, cudaMemcpyHostToDevice);
  }

  void download(const Tensor* t, void* host_data, size_t bytes) {
    cudaMemcpy(host_data, t->data_ptr(), bytes, cudaMemcpyDeviceToHost);
  }

  // Run the shim and return the output tensor (asserts success).
  Tensor* run(
      Tensor* A,
      Tensor* ql,
      Tensor* qh,
      Tensor* scale,
      Tensor* scale_step,
      Tensor* zero,
      Tensor* zero_point_step,
      int64_t group_size) {
    Tensor* output = nullptr;
    AOTITorchError error = aoti_torch_cuda_int5_plain_mm(
        A,
        ql,
        qh,
        scale,
        scale_step,
        zero,
        zero_point_step,
        group_size,
        &output);
    EXPECT_EQ(error, Error::Ok);
    EXPECT_NE(output, nullptr);
    return output;
  }

  // Check output bf16 values against expected, with absolute tolerance.
  void check_bf16_output(
      Tensor* output,
      const uint16_t* expected_data,
      int64_t count,
      float atol = 0.1f) {
    std::vector<uint16_t> actual(count);
    download(output, actual.data(), count * sizeof(uint16_t));
    cudaDeviceSynchronize();

    for (int64_t i = 0; i < count; i++) {
      uint32_t actual_bits = static_cast<uint32_t>(actual[i]) << 16;
      uint32_t expected_bits = static_cast<uint32_t>(expected_data[i]) << 16;
      float actual_f, expected_f;
      memcpy(&actual_f, &actual_bits, sizeof(float));
      memcpy(&expected_f, &expected_bits, sizeof(float));

      EXPECT_NEAR(actual_f, expected_f, atol)
          << "Mismatch at index " << i << ": actual=" << actual_f
          << " expected=" << expected_f;
    }
  }
};

